# Objetivo: definir e usar funções com parâmetros e retorno

# Função que calcula a área de um retângulo
def calcular_area(base, altura):
    """
    Recebe base e altura e retorna a área do retângulo.
    """
    return base * altura

# Chamada da função
resultado = calcular_area(5, 10)

# Exibir resultado
print("Área do retângulo:", resultado)
