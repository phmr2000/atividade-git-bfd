# operacao_arimeticas.py
# Objetivo: demonstrar operações aritméticas básicas

a = 10
b = 3

# Operações
soma = a + b            # adição
subtracao = a - b       # subtração
multiplicacao = a * b   # multiplicação
divisao_real = a / b    # divisão real
divisao_inteira = a // b # divisão inteira
resto = a % b           # módulo (resto da divisão)
potencia = a ** b       # exponenciação

# Exibir resultados
print("Soma:", soma)
print("Subtração:", subtracao)
print("Multiplicação:", multiplicacao)
print("Divisão real:", divisao_real)
print("Divisão inteira:", divisao_inteira)
print("Resto:", resto)
print("Potência:", potencia)
