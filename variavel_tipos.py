# variavel_tipos.py
# Objetivo: declarar variáveis de diferentes tipos e mostrar conversão de float para int

# Variáveis de diferentes tipos
inteiro = 10               # int
decimal = 3.7              # float
texto = "Olá, Python!"     # string
verdadeiro = True          # boolean

# Conversão de float para int (parte decimal é truncada)
conversao = int(decimal)

# Exibir resultados
print("Inteiro:", inteiro)
print("Decimal:", decimal)
print("Texto:", texto)
print("Booleano:", verdadeiro)
print("Float convertido para int (parte decimal truncada):", conversao)
